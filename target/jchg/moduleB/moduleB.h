#ifndef MODULE_B_H
#define MODULE_B_H

/**
 * @brief 2つの値を減算します
 * @param a 最初の値
 * @param b 2番目の値
 * @return a から b を引いた値
 */
s4 moduleB_subtract(s4 a, s4 b);

/**
 * @brief 2つの値を除算します
 * @param a 被除数
 * @param b 除数
 * @return a を b で除いた値
 */
s4 moduleB_divide(s4 a, s4 b);

#endif /* MODULE_B_H */
